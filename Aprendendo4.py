# Int -> Números inteiros
# Pode ser qualquer número, desde que não tenha casas decimais, exemplo:

print (11, -10, 2003, -59498)# -> Números int

# Os sinais podem ser negativos ou positivos
# Caso não tenha um sinal, o python vai considerar que o número é positivo

# Float -> números com casas decimais
# Os sinais podem ser negativos ou positivos
# Caso não tenha um sinal, o python vai considerar que o número é positivo
# Para separar o número inteiro da casa decimal, se utiliza o ponto (.), a virgula (,) só é utilizada para separar

print(12.4, 54.6, -539.8)# -> Números Float

# Se tem casa decimal é "floated ou float", se não tiver é "int"

# A 'Função' (type), é usada para falar o tipo. Exemplo:
print(type(23.4))# -> vai mostrar que é do tipo "floated"
print (type(123))# -> Vai mostrar que é do tipo "int"
print (type('Texto'))# -> Vai mostrar que é do tipo "str"

# Pode usar vários "type()" dentro do print
