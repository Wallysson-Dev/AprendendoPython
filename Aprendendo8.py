# Operadores Aritméticos
# "+" -> soma, "-" -> subtrai, "*" -> multiplica, "/" -> divide
# "//" -> divisão inteira, "**" -> exponenciação
# "%" -> módulo, que mostra o resto da divisão

adicao = 10 + 10
print ('Adição:', adicao)

subtracao = 10 - 10
print ('Subtração:', subtracao)

multiplicacao = 10 * 10
print ('Multiplicação:', multiplicacao)

divisao = 10 / 10
print ('Divisão:', divisao)

divisao_inteira = 10 // 9.7
print ('Divisão Inteira:', divisao_inteira)# Na divisão inteira, ela vai mostrar apenas o número inteiro, ele "corta" os números decimais

exponenciacao = 2 ** 10
print ('Exponenciação:', exponenciacao)

modulo = 25 % 2
print ('Módulo:', modulo)# Ele mostra o resto, no caso de 25/2, o resto da divisão é 1
# Um exemplo do uso do módulo, é para saber se um número é divisivel por outro, exemplo:
print (100 % 5) # Sempre que o resto for 0, significa que ele é divisivel, caso não seja 0, ele não é divisivel