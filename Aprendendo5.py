# Bool (boolean), é a classe que "questiona"
# Cujo a resposta só pode ser sim (true) ou não (false)
# Existe vários operadores para "questionar"
# Um deles é o "==", que é um operador lógico
# Ou seja, ele questina se um valor é igual ao outro valor

print (10 == 10)# -> vai mostrar (true), já que o primeiro valor é igual ao segundo
print (10 == 40)# -> vai mostrar (false), já que o primeiro valor não é igual ao segundo
print (type(10 == 10))# -> vai mostrar a classe "bool"

# True e False também é considerado da classe "bool"