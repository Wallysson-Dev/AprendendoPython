'''
Laço de repetição, for/in

while se usa quando não sabemos o fim da execução, exemplo
'Quantas pessoas terão o nome registrado? (Não sabemos, nesse caso se utiliza while)

'Quantas letras tem no alfabeto? (Nesse caso, sabemos que o código acaba quando o indice for igual a 27, então aqui, usamos o for/in)

for = para cada
in = em
'''

# Exemplo de for/in

Teste = 'Testando'
for letra in Teste:
    print(letra)
'''
a variavel é criada junto do for, traduzindo o código:

Teste = 'Testando'
Para cada letra em Teste:
    mostre(letra)
'''